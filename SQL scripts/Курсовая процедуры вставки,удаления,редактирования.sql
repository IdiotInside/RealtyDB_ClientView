CREATE PROCEDURE [Предложение_выборка]
	@ID_недвижимости bigint,
	@ID_клиента bigint,
	@ID_застройщика bigint,
	@Желаемая_сумма decimal (19,2),
	@Цель bit
AS
SELECT [ФИО] AS 'Клиент', [Клиент].[Контакты] AS 'Контактные данные клиента', 
              [Тип здания], [Застройщик].[Название] AS 'Застройщик', 
			  [Застройщик].[Контакты] AS 'Контактные данные застройщика', [Желаемая сумма], [Цель]
FROM [Предложение]
         INNER JOIN [Недвижимость] ON [Предложение].[ID недвижимости] = [Недвижимость].[ID]
		 INNER JOIN [Клиент] ON [Предложение].[ID клиента] = [Клиент].[ID]
		 INNER JOIN [Застройщик] ON [Предложение].[ID застройщика] = [Застройщик].[ID]
WHERE 
 ([Желаемая сумма] = @Желаемая_сумма) AND
 ([Цель] = @Цель)
GO

