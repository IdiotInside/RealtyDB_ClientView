CREATE PROCEDURE [Спрос_выборка]
	@ID_недвижимости bigint,
	@ID_клиента bigint,
	@Располагаемая_сумма decimal (19,2),
	@Цель bit
AS
SELECT [ФИО] AS 'Клиент', [Контакты] AS 'Контактные данные', [Тип здания], [Располагаемая сумма], [Цель]
FROM [Спрос]
         INNER JOIN [Недвижимость] ON [Спрос].[ID недвижимости] = [Недвижимость].[ID]
		 INNER JOIN [Клиент] ON [Спрос].[ID клиента] = [Клиент].[ID]
WHERE 
 ([Располагаемая сумма] = @Располагаемая_сумма) AND
 ([Цель] = @Цель)
GO 

